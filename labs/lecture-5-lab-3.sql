SELECT E.LAST_NAME , E.JOB_ID , E.DEPARTMENT_ID ,D.DEPARTMENT_NAME 
FROM HR.EMPLOYEES e JOIN HR.DEPARTMENTS d
ON E.DEPARTMENT_ID = D.DEPARTMENT_ID 
JOIN HR.LOCATIONS l
ON d.LOCATION_ID = l.LOCATION_ID
WHERE LOWER(l.CITY) = 'toronto' ;

