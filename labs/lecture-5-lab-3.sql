SELECT w.LAST_NAME "Employee", w.EMPLOYEE_ID "Emp#", m.LAST_NAME "Manager" , m.EMPLOYEE_ID 
FROM hr.EMPLOYEES w JOIN HR.EMPLOYEES m
ON (w.MANAGER_ID  = m.EMPLOYEE_ID) ;

